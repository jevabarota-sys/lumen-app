  static Map<String, String> getDailyQuote() {
    final now = DateTime.now();
    final dayOfYear = now.difference(DateTime(now.year, 1, 1)).inDays;
    final index = dayOfYear % _quotes.length;
    return _quotes[index];
  }

  static Map<String, String> getRandomQuote() {
    final now = DateTime.now();
    final index = now.millisecondsSinceEpoch % _quotes.length;
    return _quotes[index];
  }

  static List<Map<String, String>> getQuotesByCategory(String category) {
    switch (category.toLowerCase()) {
      case 'love':
        return _quotes
            .where((quote) =>
                quote['quote']!.toLowerCase().contains('love') ||
                quote['quote']!.toLowerCase().contains('heart') ||
                quote['quote']!.toLowerCase().contains('marriage') ||
                quote['quote']!.toLowerCase().contains('relationship'))
            .toList();
      case 'success':
        return _quotes
            .where((quote) =>
                quote['quote']!.toLowerCase().contains('success') ||
                quote['quote']!.toLowerCase().contains('achieve') ||
                quote['quote']!.toLowerCase().contains('goal') ||
                quote['quote']!.toLowerCase().contains('work'))
            .toList();
      case 'motivation':
        return _quotes
            .where((quote) =>
                quote['quote']!.toLowerCase().contains('dream') ||
                quote['quote']!.toLowerCase().contains('believe') ||
                quote['quote']!.toLowerCase().contains('possible') ||
                quote['quote']!.toLowerCase().contains('future'))
            .toList();
      case 'wisdom':
        return _quotes
            .where((quote) =>
                quote['quote']!.toLowerCase().contains('life') ||
                quote['quote']!.toLowerCase().contains('wisdom') ||
                quote['quote']!.toLowerCase().contains('learn') ||
                quote['quote']!.toLowerCase().contains('experience'))
            .toList();
      default:
        return _quotes;
    }
  }

  static List<Map<String, String>> getAllQuotes() {
    return List.from(_quotes);
  }

  static Map<String, String> getQuoteByAuthor(String author) {
    try {
      return _quotes.firstWhere((quote) =>
          quote['author']!.toLowerCase().contains(author.toLowerCase()));
    } catch (e) {
      return getDailyQuote();
    }
  }
